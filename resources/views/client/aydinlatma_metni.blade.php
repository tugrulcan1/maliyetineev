    <h2 style="font-weight: bold;color: #000;font-size: 24px;">KVKK POLİTİKASI</h2>
    <h3 style="font-family: helvetica neue;font-size: 1.5rem;line-height: 1.4;" >1. KİŞİSEL VERİLERİN KORUNMASININ AMACI</h3>
     
    <P>
        Türkiye Cumhuriyeti Anayasası’nın 20. maddesi uyarınca, herkes kendisi ile ilgili kişisel verilerin korunmasını talep etme hakkına sahiptir. 
        Bu hak, kişinin kendisiyle ilgili kişisel veriler hakkında bilgilendirilme, bu verilere erişme, bunların düzeltilmesini veya silinmesini talep etme 
        ve amaçları doğrultusunda kullanılıp kullanılmadığını öğrenmeyi de kapsar.
    </P>
    <P>
        6698 Sayılı Kişisel Verilerin Korunması Kanunu’nda (“Kanun”) ilgili kişi olarak tanımlanan kişisel veri sahiplerine 
        (“Bundan sonra “veri sahibi” olarak anılacaktır), Kanun’un 11’inci maddesinde kişisel verilerinin işlenmesine ilişkin belirli haklar tanınmıştır.
    </P>
    <P>

        İlgili Kişi kendisine tanınan bu hakları veri sorumlusu olan Şirketimize (MASTER GİRİŞİM BİLGİ TEKNOLOJİLERİ GAYRİMENKUL YATIRIM VE PAZARLAMA A.Ş) 
        (Maliyetine ev olarak anılacaktır.) bu haklara ilişkin olarak yapılacak başvuruların yazılı olarak veya Kişisel Verileri Koruma Kurumu 
        (“Kurum”) tarafından belirlenen diğer yöntemlerle tarafımıza iletilmesi gerekmektedir.
    </P>

    <h3 style="font-family: helvetica neue;font-size: 1.5rem;line-height: 1.4;" >2. KİŞİSEL VERİLERİ SAKLANAN İLGİLİNİN ŞİRKETE BAŞVURU HAKKI </h3>
    <P>2.1. Başvuru Hakkının Kapsamı    </P>


    <P>
        KVKK’nın 11. maddesi uyarınca, kişisel verileri işlenen herkes Şirketimize başvurarak kendisi ile ilgili aşağıda yer alan konularla ilgili taleplerde bulunabilir:
    </P>

    <P>
        1. Kişisel verilerinin işlenip işlenmediğini öğrenme,
    </P>
    <P>
        2. Kişisel verileri işlenmişse buna ilişkin bilgi talep etme,
    </P>
    <P>
        3. Kişisel verilerinin işlenme amacı ve bunların amacına uygun kullanılıp kullanılmadığını öğrenme,
    </P>
    <P>   
        4. Kişisel verilerinin yurt içinde veya yurt dışında aktarıldığı üçüncü kişileri öğrenme,
    </P>
    <P>
        5. Kişisel verilerinin eksik veya yanlış işlenmiş olması halinde bunların düzeltilmesini isteme ve bu kapsamda yapılan işlemin kişisel verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme,
    </P>
    <P>    
6. Kişisel verilerinin işlenmesini gerektiren sebeplerin ortadan kalkması halinde bunların silinmesini, yok edilmesini veya anonim hale getirilmesini isteme ve bu kapsamda yapılan işlemin kişisel verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme,
</P>
<P>
7. İşlenen verilerinin münhasıran otomatik sistemler vasıtasıyla analiz edilmesi suretiyle ilgili kişinin aleyhine bir sonucun ortaya çıkmasına itiraz etme,
</P>
<P></P>
8. Kişisel verilerinin kanuna aykırı olarak işlenmesi sebebiyle zarara uğraması halinde zararın giderilmesini talep etme.
</P>
<P>
2.2. Başvuru Hakkının Kapsamı Dışında Kalan Haller</P>
<P>
KVKK’nın 28. maddesi gereğince, aşağıdaki hallerde KVKK hükümleri uygulanmamaktadır:
</P>
<P>
1. Kişisel verilerinin, üçüncü kişilere verilmemek ve veri güvenliğine ilişkin yükümlülüklere
</P>
<P>
2. uyulmak kaydıyla gerçek kişiler tarafından tamamen kendisiyle veya aynı konutta yaşayan aile
</P>
<P>
3. fertleriyle ilgili faaliyetler kapsamında işlenmesi,
</P>
<P>
4. Kişisel verilerinin resmi istatistik ile anonim hâle getirilmek suretiyle araştırma, planlama ve istatistik gibi amaçlarla işlenmesi,
</P>
<P>
5. Kişisel verilerinin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini, ekonomik güvenliği, özel hayatın gizliliğini veya kişilik haklarını ihlal etmemek ya da suç teşkil etmemek kaydıyla, sanat, tarih, edebiyat veya bilimsel amaçlarla ya da ifade özgürlüğü kapsamında işlenmesi,
</P>
<P>

6. Kişisel verilerinin millî savunmayı, millî güvenliği, kamu güvenliğini, kamu düzenini veya ekonomik güvenliği sağlamaya yönelik olarak kanunla görev ve yetki verilmiş kamu kurum ve kuruluşları tarafından yürütülen önleyici, koruyucu ve istihbarî faaliyetler kapsamında işlenmesi,
</P>
<P>
7. Kişisel verilerinin soruşturma, kovuşturma, yargılama veya infaz işlemlerine ilişkin olarak yargı makamları veya infaz mercileri tarafından işlenmesi.

<P>
KVKK’nın 28. maddesinin 2. fıkrası gereğince kişisel veri sahiplerinin haklarını (zararın giderilmesini talep etme hakkı hariç olmak üzere) ileri sürmeleri mümkün olamayacaktır:
</P>
<P>
1. Kişisel veri işlemenin suç işlenmesinin önlenmesi veya suç soruşturması için gerekli olması.
</P>
<P>
2. İlgili kişinin kendisi tarafından alenileştirilmiş kişisel verilerin işlenmesi.
</P>
<P>
3. Kişisel veri işlemenin kanunun verdiği yetkiye dayanılarak görevli ve yetkili kamu kurum ve kuruluşları ile kamu kurumu niteliğindeki meslek kuruluşlarınca, denetleme veya düzenleme görevlerinin yürütülmesi ile disiplin soruşturma veya kovuşturması için gerekli olması.
</P>
<P>
4. Kişisel veri işlemenin bütçe, vergi ve mali konulara ilişkin olarak Devletin ekonomik ve mali çıkarlarının korunma sı için gerekli olması.
</P>

<h3 style="font-family: helvetica neue;font-size: 1.5rem;line-height: 1.4;" >3. ŞİRKETE BAŞVURU YÖNTEMİ</h3>
<P>
İlgili kişilerin, bu haklarına ilişkin başvurularını, Kanunu’nun 13. maddesine uygun bir biçimde, yazılı olarak veya Kişisel Verilerin Korunması Kurulu (“KVK Kurulu”) 
tarafından belirlenen diğer yöntemlerle Şirketimizin genel merkezinin bulunduğu “Cevizli, Çanakkale Cd. No:103A, 34865 Kartal/İstanbul” adresine iletmeleri gerekmektedir. 

Şirketimize “yazılı” olarak yapılacak başvurular, aşağıda belirlenen yöntemlerden biri ile ve Türkçe olara yapılacaktır. Başvurunuzla ilgili gerekli bilgi ve belgeleri tarafımıza iletiniz. İlgili kişi tarafından başvuru yapılması esnasında başvuruya ilişkin talep konusunun “Kişisel Verilerin Korunması Kanunu Kapsamında Bilgi Talebi” şeklinde açık ve net olarak belirtilmesi gerekmektedir.
</P>
<P>
Kişisel veri sahipleri, KVKK uyarınca yazılı olarak veya KVK Kurulu’nun belirleyeceği diğer yöntemlerle (Başvuru sahibinin bizzat gelerek kimliğini tevsik edici belge ile başvurması, noter vasıtasıyla, Tebligat, güvenli elektronik imza ile imzalanarak kayıtlı elektronik posta (kep) , E-posta vasıtasıyla, iadeli taahhütlü mektupla) MASTER GİRİŞİM BİLGİ TEKNOLOJİLERİ GAYRİMENKUL YATIRIM VE PAZARLAMA A.Ş’ye başvurarak bilgi talep edebilir.
</P>

<h3 style="font-family: helvetica neue;font-size: 1.5rem;line-height: 1.4;" >4. ŞİRKETİMİZİN BAŞVURULARA CEVAP VERME USULÜ, SÜRESİ VE KURULA ŞİKAYET HAKKI</h3>
<P>
Kişisel verilerinizin güvenliğini sağlamak amacıyla bilgi edinme başvurunuzun ulaşmasının ardından Şirketimiz, veri sahibi olduğunuzu teyit etmek amacıyla sizinle iletişime geçebilecek, 
bu hususta sizlerden bazı belge ve bilgiler talep edebilecektir.</P>

<P>
<span style="font-weight: 700;">MASTER GİRİŞİM BİLGİ TEKNOLOJİLERİ GAYRİMENKUL YATIRIM VE PAZARLAMA A.Ş</span>, bilgi talebini niteliğine göre en kısa sürede ve en geç 30(otuz) gün içerisinde ücretsiz olarak yanıtlayacaktır. Ancak işlemin ayrıca bir maliyet gerektirmesi halinde, Kurulca belirlenen tarifedeki ücret, ilgili kişiden talep edilebilecektir.
</P>
<P>
<span style="font-weight: 700;">MASTER GİRİŞİM BİLGİ TEKNOLOJİLERİ GAYRİMENKUL YATIRIM VE PAZARLAMA A.Ş</span>, başvuruları kabul ederek gereken işlemleri gerçekleştirecek ya da başvuruları gerekçeli olarak reddedecektir.
</P>
<P>
Kişisel veri sahibi, başvurusunun reddedilmesi, verilen cevabı yetersiz bulması veya başvuruya cevap verilmemesi hallerinde cevabı öğrendiği tarihten itibaren 30 (otuz) ve her halde başvuru tarihinden 60 (altmış) gün içerisinde KVK Kurulu’na şikayette bulunabilir.
</P>
<P>
<span style="font-weight: 700;">MASTER GİRİŞİM BİLGİ TEKNOLOJİLERİ GAYRİMENKUL YATIRIM VE PAZARLAMA A.Ş. </span>, yaşanabilecek şikayet ve sair mağduriyetleri önlemek adına usulune uygun şekilde yapılmış başvurulara süresi içinde cevap verme hassasiyeti göstermektedir.
</P>
<P>
Kişisel verilerinizin güvenliğini sağlamak amacıyla, Şirketimiz tarafından, başvuruda bulunan kişiden kimlik ve yetki tespiti amacıyla bilgi talep edebilecektir.
</P>
